n = 7  #Задаємо розмірність матриці

#Створюємо порожній список 
a = [[0] * n for _ in range(n)]

#Заповнення матриці за умовою
for j in range(n):  
    for i in range(n):  
        if i >= j:  
            a[j][i] = (n - i + j)  

#Виводимо кожен рядок матриці
for r in a:  
    print(*r)  #Виводимо елементи рядка через пробіл
